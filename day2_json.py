# 1. JSON이란?
# 경량 데이터교환형식이다.
# Javascript에서 객체를 만들 때 사용하는 표현식을 의미한다.
# JSON 표현식은 사람, 기계가 모두 이해하기 쉬우며 용량이 작아서, 최근에는 JSON이 XML을 대체해서 데이터전송등에 많이 사용되어지고있음.
# 특정 언어에 종속되지 않고, 대부분의 프로그래밍 언어에서 라이브러리를 제공해준다.

